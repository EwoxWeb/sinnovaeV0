					<h2>Bienvenue sur la page des règles du Shiny Community's Challenge !</h2>
					<br/>
					<h4>Objectif : Compléter tous ensemble une shiny card visible dans le menu déroulant "SCC" puis dans "Shiny card"!</h4>
					<br/>

					Déroulement : Entendez vous avec les gens dans le discord pour savoir qui shasse quoi, shassez votre pokémon, et une fois que vous l'avez trouvé, publiez 2 photos dans le salon "Shiny Dex Challenge" du <a href="https://discord.gg/JFPrVQV"><B>Discord  de PokéStorm</B></a>
					<br/>
					La 1ère sera une photo de votre shiny en combat ou à son apparition pour le masuda, les expeditions dynamax et certains reset.<br/>
					La 2ème sera une photo du résumé de votre pokémon sur lequel on pourra voir la date à laquelle il a été obtenu (pensez à mettre votre console à la bonne date ou le shiny ne sera pas validé !!!)
					<br/>
					Une fois les 2 photos postés, un modérateur interviendra (avec parfois un certain délai) pour valider votre shiny qui sera alors ajouté à la shiny card avec votre pseudo juste en dessous ! 
					<br/>
					<br/>
					Précisions : 
					<br/><br/>
					1-Vous ne pourrez shasser que deux pokémon maximum par jours pour ce challenge (si vous en trouvez un 3ème et que vous le publiez le lendemain ça marche pas) !<br/>
					2-Les émulateurs sont interdits<br/>
					3-Toutes les méthodes de shasses sont autorisés SAUF l'Ultra-Brèche,les Raid sur pokémon Épée et Bouclier et la shasse sur Pokémon GO.<br/>
					4-Vérifiez VRAIMENT la date de vos consoles, vous pourriez vous faire refuser un shiny que vous auriez mis des jours à shasser ! (1 jour de délai peut être laissé)<br/> 
					5-Les dynamax et méga évolutions sont comprises avec le pokémon concerné par celles-ci donc pas besoin de le shasser 2 fois. <br/>
					6-Certaines formes ne nécessiterons pas d'être toutes trouvés. C'est le cas pour Zarbi, Prismillon,Spinda et Vivaldaim ainsi que son évolution.<br/>
					7-Il n'y a aucune récompenses ! On fait ça pour le fun ^^<br/>
					8-Si vous en avez, vous pouvez shasser avec plusieures consoles. <br/>
					9-Les pokémon shiny lock ne feront pas parti de cette shiny card. Mais une shiny card bonus sera mise à disposition tout de même.<br/>
					<br/>
					<br/>
					Et pour les évolution ?
					<br/>
					Pour les évolutions, tant qu'elles sont trouvable dans pokémon X&Y ou un jeu plus récent, vous êtes dans l'obligation de les shasser. Par exemple si je veux avoir Elekable, je devrais directement le shasser dans pokémon Épée.<br/> Mais si il n'était trouvable nul part, je devrais shasser élékid ou élektek et le faire évoluer en élekable.<br/><br/>

